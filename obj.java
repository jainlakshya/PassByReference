package learning;

public class obj {
	
	private int one;
	private int two;
	
	public int getOne() {
		return one;
	}
	public void setOne(int one) {
		this.one = one;
	}
	public int getTwo() {
		return two;
	}
	public void setTwo(int two) {
		this.two = two;
	}
	
	@Override
	public String toString() {
		return "obj [one=" + one + ", two=" + two + "]";
	}

}
